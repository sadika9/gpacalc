function EnrolledCourse(year, semester, course, repeat, grade, include_in_overall_gpa, include_in_subject_gpa) {
    this.year = year;
    this.semester = semester;
    this.course = course;
    this.repeat = repeat;
    this.grade = grade;
    this.include_in_overall_gpa = include_in_overall_gpa;
    this.include_in_subject_gpa = include_in_subject_gpa;
}
